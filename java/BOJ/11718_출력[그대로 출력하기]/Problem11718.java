import java.util.*;

public class Problem11718 {
    public static void main(String arg[]) {
        Scanner input = new Scanner(System.in);
        StringBuilder sb = new StringBuilder();

        while (input.hasNext()) {
            sb.append(input.nextLine() + "\n");
        }
        System.out.println(sb.toString());
    }
}